#!/usr/bin/env python3
"""
This module defines a function that returns the string representation of a float.
"""

def to_str(n: float) -> str:
    """
    Returns the string representation of a floating-point number.

    Args:
        n (float): The floating-point number to convert to a string.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
